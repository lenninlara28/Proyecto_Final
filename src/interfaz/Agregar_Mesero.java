    private void cmdGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdGuardarActionPerformed
        String cedula, nombre, apellido, codigo;
        cedula = txtCedula.getText();
        nombre = txtNombre.getText();
        apellido = txtApellido.getText();
        codigo = txtCodigo.getText();
        ArrayList<Mesero> meseroModificado;
        if (txtCedula.getText().trim().isEmpty() || txtNombre.getText().trim().isEmpty()
                || txtApellido.getText().trim().isEmpty() || txtCodigo.getText().trim().isEmpty()) {
            Helper.mensaje(this, "Llenar Todos Los Espacios", 1);
        } else {
            try {
                if (aux == 0) {
                    Mesero m = new Mesero(cedula, nombre, apellido, codigo);
                    m.guardar(salida);
                } else {
                    meseroModificado = Helper.modificarMesero(ruta, cedula, nombre, apellido, codigo);
                    salida = new ObjectOutputStream(new FileOutputStream(ruta));
                    Helper.volcado(salida, meseroModificado);
                    aux = 0;
                    Helper.mensaje(this, "Mesero Actualizado Correctamente!", 1);
                }
            } catch (IOException ex) {
                Logger.getLogger(Agregar_Mesero.class.getName()).log(Level.SEVERE, null, ex);
            }
            Helper.llenarTablaMesero(tblTablaMeseros, ruta);
            txtCedula.setText("");
            txtNombre.setText("");
            txtApellido.setText("");
            txtCodigo.setText("");
            txtCedula.requestFocusInWindow();
            JButton botonesH[] = {cmdBuscar, cmdCancelar};
            JButton botonesD[] = {cmdEliminar, cmdGuardar};
            JTextField txtD[] = {txtNombre, txtApellido, txtCodigo};
            Helper.deshabilitarJTextField(txtD);
            Helper.habilitarBotones(botonesH);
            Helper.deshabilitarBotones(botonesD);
            txtCedula.setEditable(true);
        }
    }//GEN-LAST:event_cmdGuardarActionPerformed

    private void cmdEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdEliminarActionPerformed
        int i, op;
        op = JOptionPane.showConfirmDialog(this, "Â¿Seguro que desea eliminar a este mesero?", "Eliminar", JOptionPane.YES_NO_OPTION);
        ArrayList<Mesero> meseros = Helper.traerDatos(ruta);
        if (op == JOptionPane.YES_OPTION) {
            i = tblTablaMeseros.getSelectedRow();
            meseros.remove(i);
            try {
                salida = new ObjectOutputStream(new FileOutputStream(ruta));
            } catch (FileNotFoundException ex) {
                Logger.getLogger(Agregar_Mesero.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(Agregar_Mesero.class.getName()).log(Level.SEVERE, null, ex);
            }
            Helper.volcado(salida, meseros);
            Helper.llenarTablaMesero(tblTablaMeseros, ruta);
            txtCedula.setText("");
            txtNombre.setText("");
            txtApellido.setText("");
            txtCodigo.setText("");
            txtCedula.requestFocusInWindow();
            JButton botonesH[] = {cmdBuscar, cmdCancelar};
            JButton botonesD[] = {cmdEliminar, cmdGuardar};
            Helper.habilitarBotones(botonesH);
            Helper.deshabilitarBotones(botonesD);
            JTextField txtD[] = {txtNombre, txtApellido, txtCodigo};
            Helper.deshabilitarJTextField(txtD);
            txtCedula.setEditable(true);
        } else {
        }
    }//GEN-LAST:event_cmdEliminarActionPerformed

