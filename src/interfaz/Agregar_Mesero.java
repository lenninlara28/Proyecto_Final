    private void cmdBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdBuscarActionPerformed
        String cedula;
        cedula = txtCedula.getText();
        Mesero m;
        if (txtCedula.getText().trim().isEmpty()) {
            Helper.mensaje(this, "Ingrese Cedula ", 2);
        } else {
            if (Helper.buscarMeseroCedula(cedula, ruta)) {
                m = Helper.traerMeseroCedula(cedula, ruta);
                txtNombre.setText(m.getNombre());
                txtApellido.setText(m.getApellido());
                txtCodigo.setText(m.getCodigo());
                aux = 1;
            } else {
                txtNombre.requestFocusInWindow();
                aux = 0;
            }
            JButton botonesH[] = {cmdGuardar, cmdCancelar};
            JButton botonesD[] = {cmdBuscar, cmdEliminar};
            JTextField txtA[] = {txtNombre, txtApellido, txtCodigo};
            Helper.habilitarBotones(botonesH);
            Helper.deshabilitarBotones(botonesD);
            Helper.habilitarJTextField(txtA);
            txtCedula.setEditable(false);
        }
    }//GEN-LAST:event_cmdBuscarActionPerformed

    private void tblTablaMeserosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTablaMeserosMouseClicked
        int i;
        Mesero m;
        ArrayList<Mesero> meseros = Helper.traerDatos(ruta);
        i = tblTablaMeseros.getSelectedRow();
        m = meseros.get(i);
        txtCedula.setText(m.getCedula());
        txtNombre.setText(m.getNombre());
        txtApellido.setText(m.getApellido());
        txtCodigo.setText(m.getCodigo());
        aux = 1;
        JButton botonesH[] = {cmdEliminar, cmdGuardar, cmdCancelar};
        JButton botonesD[] = {cmdBuscar};
        JTextField txtA[] = {txtNombre, txtApellido, txtCodigo};
        Helper.habilitarJTextField(txtA);
        Helper.habilitarBotones(botonesH);
        Helper.deshabilitarBotones(botonesD);
        txtCedula.setEditable(false);
    }//GEN-LAST:event_tblTablaMeserosMouseClicked

