    private void cmdOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdOkActionPerformed
        String nombre, codigo;
        int aux = 0;
        nombre = cmbNombre.getSelectedItem().toString();
        codigo = txtCodigo.getText();
        ArrayList<Mesero> meseros;
        meseros = Helper.traerDatos(rutaM);
        for (int i = 0; i < meseros.size(); i++) {
            if (meseros.get(i).getNombre().equals(nombre) && meseros.get(i).getCodigo().equals(codigo)) {
                JButton botonesA[] = {cmdMesa1, cmdMesa2, cmdMesa3, cmdMesa4, cmdMesa5, cmdMesa6, cmdMesa7, cmdMesa8, cmdMesa9, cmdMesa10};
                Helper.habilitarBotones(botonesA);
                Login_Mesero.this.setVisible(false);
                Principal.jlbIngreso.setText("Mesero");
                aux = 1;
            }
        }
        if (aux == 0) {
            Helper.mensaje(this, "Nombre,Codigo o ContaseÃ±a Incorrecto\n"
                    + "Verifique e Intente Nuevamente", 3);
        }
    }//GEN-LAST:event_cmdOkActionPerformed

    private void cmdNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdNuevoActionPerformed
        ArrayList<Administrador> administrador;
        administrador = Helper.traerDatos(rutaA);
        String codigoAdmin = JOptionPane.showInputDialog("Digite El Codigo Del Administrador Para Ingresar Un Mesero");
        for (int i = 0; i < administrador.size(); i++) {
            if (administrador.get(i).getCodigo().equals(codigoAdmin)) {
                Agregar_Mesero a = new Agregar_Mesero(null, true);
                a.setVisible(true);
            } else {
                Helper.mensaje(this, "Codigo Incorrecta", 3);
            }
        }
    }//GEN-LAST:event_cmdNuevoActionPerformed

